sampleStr = "Hello World"
print(sampleStr)

# Capitalize
print(sampleStr.capitalize())

# Swap case
print(sampleStr.swapcase())

# Get lenght
print(len(sampleStr))

# Replace
print(sampleStr.replace('World', 'You'))

# Count
subStr = "o"
print(sampleStr.count(subStr))

# Split
print(sampleStr.split())

# Find
print(sampleStr.find('World'))

# Is all alphabetic
print(sampleStr.isalpha())

# Is all numeric
print(sampleStr.isalnum())