x = 10

# if condition
if x < 6:
  print('this is false')

# if else condition
if x < 6:
  print('This is false')
else:
  print('This is true')

# if elseif condition
color = 'blue'

if color == "red":
  print('Color is red')
elif color == "green":
  print('Color is green')
else:
  print('Color is blue')

# Logical operators
if color == 'blue' and x == 10:
  print('all condition are fullfilled')